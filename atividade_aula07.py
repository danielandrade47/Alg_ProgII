class Retangulo:
    def __init__(self, altura, base, area):
        self.altura = altura
        self.base = base
        self.area = (base * altura)

base = input("Digite a Base do Retangulo: ")
altura = input("Digite a Altura do Retangulo: ")
area = float(base) * float(altura)

def imprimir(self):
    print("Base do Retangulo: " + self.base)
    print("Altura do Retangulo: " + self.altura)
    print("Area do Retangulo: " + self.area)

print("Area do Retangulo: ", area)
print("Altura do Retangulo: " + altura)
print("Base do Retangulo: " + base)